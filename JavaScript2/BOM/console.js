// METHOD CONSOLE

// console.log(); => Menampilkan pesan secara umum ke console browser.
console.log("Hello World");

// console.info(); => Menampilkan pesan info ke console browser.
console.info("Hello, ini adalah info");

// console.warn(); => Menampilkan pesan warning ke console browser.
console.warn("Hello, ini adalah warning");

// console.error(); => Menampilkan pesan error ke console browser.
console.error("Hello, ini adalah error");